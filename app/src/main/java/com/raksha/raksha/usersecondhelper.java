package com.raksha.raksha;

public class usersecondhelper {
String name,phonec,reln;

    public usersecondhelper() {
    }

    public usersecondhelper(String name, String phonec, String reln) {
        this.name = name;
        this.phonec = phonec;
        this.reln = reln;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhonec() {
        return phonec;
    }

    public void setPhonec(String phonec) {
        this.phonec = phonec;
    }


    public String getReln() {
        return reln;
    }

    public void setReln(String reln) {
        this.reln = reln;
    }
}
